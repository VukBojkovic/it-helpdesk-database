-- 2. CREATE DATABASE TABLES WITH
INTEGRITY CONSTRAINTS
CREATE TABLE USERS (
user_id NUMBER GENERATED BY DEFAULT AS
IDENTITY PRIMARY KEY,
first_name VARCHAR2(50) NOT NULL,
last_name VARCHAR2(50) NOT NULL,
email VARCHAR2(100) UNIQUE NOT NULL,
department VARCHAR2(50) NOT NULL
);

CREATE TABLE TECHNICIANS (
tech_id NUMBER GENERATED BY DEFAULT AS

IDENTITY PRIMARY KEY,
first_name VARCHAR2(50) NOT NULL,
last_name VARCHAR2(50) NOT NULL,
tier_level NUMBER CHECK (tier_level IN (1, 2, 3)),
-- Support tiers (L1, L2, L3)
status VARCHAR2(15) DEFAULT 'AVAILABLE'
CHECK (status IN ('AVAILABLE', 'BUSY',
'ON_LEAVE'))
);

CREATE TABLE TICKETS (
ticket_id NUMBER GENERATED BY DEFAULT AS
IDENTITY PRIMARY KEY,
user_id NUMBER NOT NULL,
tech_id NUMBER,
title VARCHAR2(100) NOT NULL,
priority VARCHAR2(10) CHECK (priority IN
('LOW', 'MEDIUM', 'HIGH', 'CRITICAL')),
status VARCHAR2(15) DEFAULT 'OPEN' CHECK
(status IN ('OPEN', 'IN_PROGRESS', 'RESOLVED', 'CLOSED')),
created_at DATE DEFAULT SYSDATE NOT NULL,
resolved_at DATE,
CONSTRAINT fk_ticket_user FOREIGN KEY
(user_id) REFERENCES USERS(user_id) ON
DELETE CASCADE,
CONSTRAINT fk_ticket_tech FOREIGN KEY
(tech_id) REFERENCES TECHNICIANS(tech_id)
ON DELETE SET NULL
);

