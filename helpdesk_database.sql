-- 4. ANALYTICAL BUSINESS QUERY WITH JOIN
-- Goal: Get a clear overview of all active critical and high priority tickets
SELECT
t.ticket_id,
t.title AS ticket_issue,
t.priority,
t.status AS ticket_status,
u.first_name || ' ' || u.last_name AS reported_by,
u.department,
NVL(tech.first_name || ' ' || tech.last_name,
'UNASSIGNED') AS assigned_technician
FROM TICKETS t
JOIN USERS u ON t.user_id = u.user_id
LEFT JOIN TECHNICIANS tech ON t.tech_id =
tech.tech_id

WHERE t.priority IN ('HIGH', 'CRITICAL') AND
t.status != 'RESOLVED'
ORDER BY t.created_at DESC;

-- 5. PL/SQL STORED FUNCTION
-- Goal: Calculate total number of open tickets for a specific technician
CREATE OR REPLACE FUNCTION
get_tech_open_tickets(p_tech_id IN NUMBER)
RETURN NUMBER IS
v_ticket_count NUMBER := 0;
BEGIN
SELECT COUNT(*)
INTO v_ticket_count
FROM TICKETS
WHERE tech_id = p_tech_id AND status IN
('OPEN', 'IN_PROGRESS');

RETURN v_ticket_count;

END;
/

-- 6. PL/SQL DATABASE TRIGGER
-- Goal: Automatically update resolved_at date when ticket status changes to RESOLVED
CREATE OR REPLACE TRIGGER
trg_set_resolved_date
BEFORE UPDATE ON TICKETS
FOR EACH ROW
BEGIN
IF :NEW.status = 'RESOLVED' AND :OLD.status !=
'RESOLVED' THEN
:NEW.resolved_at := SYSDATE;
END IF;
END;
